#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const char	*pa;
	size_t		i;

	pa = (const char *)s;
	i = 0;
	if (!s)
		return (NULL);
	while (i < n)
	{
		if (*(unsigned char *)(s + i) == (unsigned char)c)
			return ((void *)(s + i));
		i++;
	}
	return (0);
}
